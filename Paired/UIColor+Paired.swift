import UIKit

extension UIColor {

    class func paired_greenColor() -> UIColor {
        return UIColor(red:0.55, green:0.82, blue:0.12, alpha:1)
    }
    
    class func paired_greyColor() -> UIColor {
        return UIColor(red:0.43, green:0.43, blue:0.43, alpha:1);
    }
}
